    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pwdgen?zeroDateTimeBehavior=convertToNull", "root", "");
            Statement stmt = con.createStatement();
            ResultSet rs=stmt.executeQuery("select website, email, username, password from data where uID="+uID+";");
            ResultSetMetaData md = rs.getMetaData();
            int columnCount = md.getColumnCount();
            Vector columns = new Vector(columnCount);

            //store column names
            for(int i=1; i<=columnCount; i++)
                columns.add(md.getColumnName(i));

            Vector data = new Vector();
            Vector row;

            while (rs.next()) {

                row = new Vector(columnCount);
                for(int i=1; i<=columnCount; i++)
                {
                    row.add(rs.getString(i));
                }
                data.add(row);
                DefaultTableModel tableModel = new DefaultTableModel(data, columns);
                jTable2.setModel(tableModel);

            }
            rs.close();
            con.close();

        }

        catch(Exception et)
        {

        }

    }//GEN-LAST:event_jButton6ActionPerformed

